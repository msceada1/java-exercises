package Mis_Ejercicios;

public class Piramide {
    public static void main(String[] args) {

        for (int i = 0; i < 4; i++) {

            System.out.println(" ");

            for (int j = 0; j < i + 1; j++) {

                System.out.print(" ");

                System.out.print("*");
            }
        }
    }
}
